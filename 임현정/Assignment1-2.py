number = input('숫자 2개를 입력해주세요 : ')
num1 , num2 = str(number).split(',')
num1 , num2 = int(num1), int(num2)
print('더하기 : i + j =',num1+num2)
print('빼기 : i - j =',num1-num2)
print('곱하기 : i * j =',num1*num2)
print('나누기 : i / j =',num1/num2)
print('나머지없이 : i // j =',num1//num2)
print('나머지만 : i % j =',num1%num2)