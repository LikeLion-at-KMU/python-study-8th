# 맨위의 한 줄은 한글이 깨지는 문제가 발생해서 추가했습니다.

i, j = input("숫자 두개를 입력해주세요 : ").split()

i = int(i)
j = int(j)

print(f"더하기: i + j = {i + j}")
print(f"빼기:   i - j = {i - j}")
print(f"곱하기: i * j = {i * j}")
print(f"나누기: i / j = {i / j}")
print(f"나머지없이: i // j = {i // j}")
print(f"나머지만: i % j = {i % j}")
